package sum_of_all_the_digits_in_a_number;

public class Find_Sum_of_Elements_in_Array_1_1 {

	public static void main(String[] args) {
		//Enhanced For Loop - For each loop
				int b[] = {5,2,7,9,6};
				
				int sum1 = 0;
				for(int value: b)
				{
					sum1 = sum1 + value;
				}
				System.out.println("Sum of Array Elements: " + sum1);

	}

}
